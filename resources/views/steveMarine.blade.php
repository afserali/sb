				<h4>Practice in International trade</h4>
				<p>The normal practice in export /import trade is for the exporter to ask the importer to open a letter of credit with a bank in favour of the exporter. As and when the goods are ready for shipment by the exporter, he hands over the documents of title to the bank and gets the bill of exchange drawn by him on the importer, discounted with the bank. In this process, the goods which are the subject of the sale are considered by the bank as physical security against the monies advanced by it to the exporter. A further security by way of an insurance policy is also required by the bank to protect its interests in the event of the goods suffering loss or damage in transit, in which case the importer may not make the payment. The terms and conditions of insurance are specified in the letter of credit. For export/import policies, the-Institute Cargo Clauses (I.C.C.) are used. These clauses are drafted by the Institute of London Underwriters (ILU) and are used by insurance companies in a majority of countries including India.
